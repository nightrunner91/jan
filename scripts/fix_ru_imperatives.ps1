$models = @("assistant","constructor","cook","developer","doctor","lawyer","policeman","politician","psychologist","scientist","trainer")
$base = "E:\Projects\jan\models"

foreach ($m in $models) {
    foreach ($ext in @(".ru.md", ".ru.json")) {
        $path = "$base\$m\jan-$m$ext"
        if (-not (Test-Path $path)) { Write-Warning "Missing: $path"; continue }
        
        $content = [System.IO.File]::ReadAllText($path, [System.Text.Encoding]::UTF8)
        
        foreach ($old in $replacements.Keys) {
            $content = $content.Replace($old, $replacements[$old])
        }
        
        [System.IO.File]::WriteAllText($path, $content, [System.Text.Encoding]::UTF8)
        Write-Output "Updated: $path"
    }
}
